package com;

public class outputVariables {
	//Bean class for storing the output variables
	private String lat;
	private String lon;
	private String Mint;
	private String Wspd;
	private String Wdir;
	private String pop12;	
	
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLon() {
		return lon;
	}
	public void setLon(String lon) {
		this.lon = lon;
	}
	public String getMint() {
		return Mint;
	}
	public void setMint(String mint) {
		Mint = mint;
	}
	public String getWspd() {
		return Wspd;
	}
	public void setWspd(String wspd) {
		Wspd = wspd;
	}
	public String getWdir() {
		return Wdir;
	}
	public void setWdir(String wdir) {
		Wdir = wdir;
	}
	public String getPop12() {
		return pop12;
	}
	public void setPop12(String pop12) {
		this.pop12 = pop12;
	}
	
}
